public class _01_AssignVariables {
    public static void main(String[] args) {

        boolean boolVariable = false;
        String text = "\"Palo Alto, CA\"";
        int num = 32768;
        int bigNum = 2000000000;
        double decNum = 0.1234567891011;
        float flNum = 0.5f;
        long bigNum2 = 919827112351L;
        byte littleNum = 127;
        char ch = 'c';
        System.out.println(boolVariable);
        System.out.println(text);
        System.out.println(num);
        System.out.println(bigNum);
        System.out.println(decNum);
        System.out.println(flNum);
        System.out.println(bigNum2);
        System.out.println(littleNum);
        System.out.println(ch);
    }
}
